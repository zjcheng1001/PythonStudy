package com.grammar;

public class IfGrammar {

    public static void main(String[] args) {
        char a = 'C';
        if (a == 'A') {
            System.out.println("a is A");
        } else if (a == 'B') {
            System.out.println("a is B");
        } else {
            System.out.println("a is C");
        }
    }
}
